list = ["a","b","c","d","e","f","g","h"]

print(list[2:5]) # not including index 5, including index 2
print(list[2:]) # index 2 and the rest of it
print(list[:3]) # not including index 3, all the elements before
print(list[2:5:2])
print(list[::-1]) # reverses the list


